function subtractMinutes(base: Date, minutes: number): string {
  return new Date(base.getTime() - minutes * 60_000).toISOString();
}

function addHours(base: Date, hours: number): string {
  return new Date(base.getTime() + hours * 60 * 60_000).toISOString();
}

function createPulseMetrics(events: DeltaEvent[]): PulseMetric[] {
  const summary = buildDeltaSummary(events);

  return [
    {
      label: "new jobs",
      value: String(summary.newJobs),
      tone: "hot",
      note: "fresh since your last visit",
    },
    {
      label: "status moves",
      value: String(summary.statusMoves),
      tone: "cool",
      note: "employers or applications changed",
    },
    {
      label: "trust shifts",
      value: String(summary.trustShifts),
      tone: "watch",
      note: "salary, proof, or risk changed",
    },
    {
      label: "move now",
      value: String(summary.urgentNow),
      tone: "hot",
      note: "time-sensitive items worth checking now",
    },
  ];
}

